const { Schema, model } = require('mongoose');

const PizzaSchema = new Schema({
    pizzaName: {
        type: String
    },
    createdBy: {
        type: String
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    size: {
        type: String,
        default: 'Large'
    },
    toppings: []
});

//Creates the Pizza Model using PizzaSchema
const Pizza = model('Pizza', PizzaSchema);

//Exports the model
module.exports = Pizza;